#include <iostream>
using namespace std;

struct Buku {
	int no;
	string nama;
	int harga;
	};
	
	void SequentialTanpaSentinel (Buku daftar [], int n, int cari) { 
		bool found = false;
		for (int i=0; i<n; i++) {
			if (daftar[i].no == cari) {
		cout << " Buku " << daftar [i].nama << " Ditemukan" << endl;
		cout << " Harga: Rp. " <<daftar[i].harga << endl;
		found = true;
		}
	}
	if (!found) { 
		cout << "Buku nomor " << cari << " tidak ditemukan" <<endl;
			}

		}
		
	  void SequentialSentinel (Buku daftar [], int n, int cari) {
		  int i = 0;
		  Buku terakhir = daftar[n-1]; 
		  daftar[n-1].no = cari; // untuk sentinel
		  while (daftar[i].no != cari) {
			  i++;
		  }
		  
		  daftar[n-1] = terakhir;
		  if ( i < n-1 || daftar[n-1].no == cari) {
			  cout << "Buku" << daftar[i].nama << " Ditemukan" << endl;
			  cout << "Harga: Rp. " << daftar[i].harga << endl;
		  } else {
			  cout << "Buku nomor " << cari << " tidak ditemukan"	<< endl;
		  }
	  }	  
		void Binary (Buku daftar[], int n, int cari ) {
			int i=0;
			int j= n-1;
			bool found = false;
			
			while (i<= j) {
				int k = (i+j) / 2; 
					if (daftar[k].no == cari) {
						cout << "Buku " << daftar[k].nama << " Ditemukan" << endl;
						cout << "Harga: Rp." << daftar[k].harga << endl;
						found = true;
						break;
						}
						else if (daftar[k].no <cari) { 
							i= k+1;
							 }
							
							else {
								j= k-1;
							
							 }
								}
							
			
		if (!found) {
			cout << "Buku nomor "<< cari << "tidak ditemukan";
		}
	}
		
